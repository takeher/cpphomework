#include<iostream>
class Solution {
public:
    int uniquePaths(int m, int n) {
        long long ans = 1;
        for (int x = n, y = 1; y < m; ++x, ++y) {
            ans = ans * x / y;
        }
        return ans;
    }
};
int main(int argc, char *argv[])
{
	int m = 7;
	int n = 3;
	Solution sol;
	int ans = sol.uniquePaths(m, n);
	std::cout<<"ans is:"<<ans<<std::endl;
	return 0;
}
